    //Mostar matriz
    public void VerLista (){
        String matriz[][]= new String [Listado2.size()][3];
        for (int i = 0; i < Listado2.size(); i++) {
            matriz [i][0]= Listado2.get(i).getNombre();
            matriz [i][1]= Listado2.get(i).getPrecio();
            matriz [i][2]= Listado2.get(i).getDescripcion();
            
        }
        tablaPizza.setModel(new javax.swing.table.DefaultTableModel(
                matriz,
            new String [] {
                "Nombre", "Descripcion ", "Precio"
            }
        ));
    }
